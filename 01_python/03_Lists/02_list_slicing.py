a = ["Iron man","Thor","Captain America","Hulk"]
print(a[2]) # Print Captain America as start from index 0
print(a[-1]) # Print Hulk as negative from back side

print(a[1])
print(a[0:3]) # Slice from index 0 to 2 give first 3 names
print(a[1:4])
print(a[0:5:2])
print(a[::-1])
print(a[:2]) # End on index 2 and start from 0
print(a[2:]) # start from index 2 upto end of data
print(a[-3:-1])
print(a[-1:-4:-1]) # reverse in given range